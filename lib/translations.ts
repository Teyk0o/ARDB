export function getStatLabel(key: string, lang: Language): string {
  // Try exact match first
  if (statLabels[lang][key]) {
    return statLabels[lang][key];
  }

  // Try lowercase version (for "Healing" -> "healing")
  const lowerKey = key.toLowerCase();
  for (const [k, v] of Object.entries(statLabels[lang])) {
    if (k.toLowerCase() === lowerKey) {
      return v;
    }
  }

  // Fallback: format as Title Case
  return key
    .replace(/([A-Z])/g, ' $1')
    .replace(/^./, (str) => str.toUpperCase())
    .trim();
}

